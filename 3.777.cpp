#include<stdio.h>
int main()
{
	float x,y,z;
	x=8.8;
	y=3.5;
	z=-5.2;
	
	
	printf("\n a.- %f",x+y+z);
	printf("\n b.- %f",2*y+3*(x-z));
	printf("\nc.- %f",x/y);
	
	printf("\n e.- %f",x/(y+z));
	printf("\n f.- %f",(x/y)+z);
	printf("\n g. %f",2*x/3*y);
	printf("\n h.- %f",2*x/(3*y));
	return 0;
}
